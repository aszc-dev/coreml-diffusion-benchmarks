"""`cleanup` command: reclaim generated benchmark state with measured sizes.

Converted models, raw power captures, result records, and the shared input are
large and fully regenerable. This module discovers what exists, measures it
exactly (no guesswork), and deletes only what the user explicitly selects. The
tracked per-build conversion-timing JSON under artifacts/ (R9 data) is always
preserved.

Pure logic (discover/measure/delete) is separated from the interactive driver so
it is testable without a TTY or questionary.
"""

import shutil
from dataclasses import dataclass
from pathlib import Path

from rich.table import Table

from sdbench.tui.console import console, human_bytes
from sdbench.tui.workspace import Workspace


@dataclass(frozen=True)
class CleanupTarget:
    key: str
    label: str
    description: str
    paths: tuple[Path, ...]
    size_bytes: int


def _path_size(path: Path) -> int:
    """Apparent size of a file or directory tree, in bytes."""
    if path.is_symlink():
        return 0
    if path.is_file():
        try:
            return path.stat().st_size
        except OSError:
            return 0
    total = 0
    for child in path.rglob("*"):
        if child.is_file() and not child.is_symlink():
            try:
                total += child.stat().st_size
            except OSError:
                pass
    return total


def _artifact_blobs(ws: Workspace) -> list[Path]:
    """Heavy converted-model payloads under artifacts/, excluding tracked JSON."""
    root = ws.artifacts_dir
    if not root.exists():
        return []
    blobs: list[Path] = []
    for pattern in ("*.mlmodelc", "*.mlpackage"):
        blobs.extend(sorted(root.rglob(pattern)))
    return blobs


def _glob_existing(directory: Path, pattern: str = "*") -> list[Path]:
    if not directory.exists():
        return []
    return [p for p in sorted(directory.glob(pattern)) if p.exists()]


def discover_targets(ws: Workspace) -> list[CleanupTarget]:
    """Build the list of reclaimable targets that actually exist on disk."""
    specs = [
        (
            "artifacts",
            "Converted CoreML models",
            "Compiled .mlmodelc / .mlpackage payloads (conversion-timing JSON is kept).",
            _artifact_blobs(ws),
        ),
        (
            "power-raw",
            "Raw power captures",
            "powermetrics plists under results/raw/ (regenerated each run).",
            _glob_existing(ws.results_raw_dir),
        ),
        (
            "results",
            "Result records + tables",
            "JSONL records and generated tables under results/data and results/tables.",
            _glob_existing(ws.results_data_dir) + _glob_existing(ws.results_tables_dir),
        ),
        (
            "shared-input",
            "Shared input",
            "Persisted latent/timestep/embedding (regenerated by prepare-input).",
            _glob_existing(ws.shared_input_dir, "*.npz"),
        ),
        (
            "cache",
            "Download cache",
            "Checkpoints downloaded and cached by sdbench.",
            _glob_existing(ws.cache_dir),
        ),
    ]
    targets: list[CleanupTarget] = []
    for key, label, description, paths in specs:
        materialized = tuple(paths)
        if not materialized:
            continue
        size = sum(_path_size(p) for p in materialized)
        targets.append(
            CleanupTarget(key=key, label=label, description=description, paths=materialized, size_bytes=size)
        )
    return targets


def _assert_within(ws: Workspace, paths: list[Path]) -> None:
    """Guard against ever deleting outside the workspace (e.g. an external checkpoint)."""
    for path in paths:
        try:
            path.resolve().relative_to(ws.root)
        except ValueError as exc:
            raise ValueError(f"Refusing to delete path outside workspace: {path}") from exc


def delete_target(target: CleanupTarget) -> int:
    """Delete a target's paths. Returns bytes freed (measured at discovery)."""
    for path in target.paths:
        if path.is_dir() and not path.is_symlink():
            shutil.rmtree(path, ignore_errors=True)
        else:
            try:
                path.unlink()
            except (FileNotFoundError, IsADirectoryError):
                pass
    return target.size_bytes


def _render_table(ws: Workspace, targets: list[CleanupTarget]) -> None:
    table = Table(title=f"Reclaimable in {ws.root}", title_style="sdbench.title", header_style="sdbench.dim")
    table.add_column("Target")
    table.add_column("Size", justify="right", style="sdbench.size")
    table.add_column("Items", justify="right")
    table.add_column("Details", style="sdbench.dim")
    for target in targets:
        table.add_row(target.label, human_bytes(target.size_bytes), str(len(target.paths)), target.description)
    table.add_section()
    total = sum(t.size_bytes for t in targets)
    table.add_row("[sdbench.title]Total[/]", f"[sdbench.size]{human_bytes(total)}[/]", "", "")
    console.print(table)


def _prompt_selection(targets: list[CleanupTarget]) -> list[CleanupTarget]:
    import questionary  # lazy: keeps the front-end import light and tests TTY-free

    choices = [
        questionary.Choice(
            title=f"{target.label:<26}{human_bytes(target.size_bytes):>10}  — {target.description}",
            value=target.key,
        )
        for target in targets
    ]
    keys = questionary.checkbox(
        "Select what to delete (space toggles, enter confirms):",
        choices=choices,
    ).ask()
    if not keys:
        return []
    chosen = set(keys)
    return [t for t in targets if t.key in chosen]


def _confirm(chosen: list[CleanupTarget], total: int, *, assume_yes: bool) -> bool:
    if assume_yes:
        return True
    import questionary

    console.print("\n[sdbench.danger]About to permanently delete:[/]")
    for target in chosen:
        console.print(f"  • {target.label} [sdbench.dim]({human_bytes(target.size_bytes)}, {len(target.paths)} item(s))[/]")
    answer = questionary.confirm(
        f"Delete these and free {human_bytes(total)}? This cannot be undone.",
        default=False,
    ).ask()
    return bool(answer)


def run_cleanup(
    ws: Workspace,
    *,
    select_all: bool = False,
    assume_yes: bool = False,
    only: list[str] | None = None,
) -> int:
    """Interactive cleanup entrypoint. Returns the number of bytes freed."""
    targets = discover_targets(ws)
    if not targets:
        console.print("[sdbench.ok]Nothing to clean — workspace is already tidy.[/]")
        return 0

    _render_table(ws, targets)

    if only:
        wanted = set(only)
        chosen = [t for t in targets if t.key in wanted]
        unknown = wanted - {t.key for t in targets}
        if unknown:
            console.print(f"[sdbench.warn]No matching target(s): {', '.join(sorted(unknown))}[/]")
    elif select_all:
        chosen = list(targets)
    else:
        chosen = _prompt_selection(targets)

    if not chosen:
        console.print("[sdbench.dim]No targets selected. Nothing deleted.[/]")
        return 0

    _assert_within(ws, [p for t in chosen for p in t.paths])
    total = sum(t.size_bytes for t in chosen)

    if not _confirm(chosen, total, assume_yes=assume_yes):
        console.print("[sdbench.dim]Aborted. Nothing deleted.[/]")
        return 0

    freed = 0
    for target in chosen:
        freed += delete_target(target)
        console.print(f"  [sdbench.ok]✓[/] {target.label} [sdbench.dim]({human_bytes(target.size_bytes)})[/]")
    console.print(f"\n[sdbench.ok]Freed {human_bytes(freed)}.[/]")
    return freed
